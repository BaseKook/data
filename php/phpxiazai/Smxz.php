智慧云谷物联网研究院“物联网智慧窨井盖监控管理系统项目”被纳入2017年度省重点研发计划！

2017-07-06 智慧云谷动态
好消息！
智慧云谷物联网研究院“物联网智慧窨井盖监控管理系统项目”被纳入2017年度省重点研发计划（公益类专项）！
随着国家城镇化、城市化步伐的进一步加快，市政公用设施建设发展迅速。市政、电力、通信等部门有大量市政设备、资产需要管理。其中窨井盖成为了不可忽视的一项。大量窨井盖丢失、破损、下水道水位监测不利造成的洪水洪涝、下水道可燃性气体爆炸等原因而导致人员伤亡的事件不断发生。

法国文学家雨果说过：下水道是城市的良心。同样，窨井盖也关乎一个城市的良心，用高科技武装起来的窨井盖，能主动防范报警，防窨井盖丢失，实时检测下水道易燃易爆等有毒气体含量，检测数据实时上传，保护人民群众安全，让城市和城市管理者有一个很好的良心，更体现了一个城市的智慧化程度。 
为更好地保障公共设施安全，迫切需要采用新技术、新模式让窨井盖“智慧升级”。 
智慧云谷是国家科技部、住建部共同指定的智慧城市智慧社区示范试点单位，智慧云谷的城市部件信息化是城市信息化的重要组成部分，是智慧城市的重要内容，是城市管理及和谐社区建设的基础环节。在推进智慧城市建设中，应积极探索，充分运用现代技术手段管理社区、服务居民，不断提升为人民服务的水平。 


随着智慧云谷物联网技术的飞速发展，窨井盖管理已经进入第四阶段即智慧阶段，通过物联网云计算技术，由井盖实时主动报警，在远程控制中心实现对窨井盖的实时监控与管理。
智慧云谷基于公司强大的技术及自主研发实力，结合窨井盖管理模式的发展变迁趋势，创造性地提出了新一代“智慧窨井盖智能监控管理系统”，使每一个窨井盖都具有智慧的大脑，成为“智慧窨井盖”。智慧窨井盖不仅有系统主动防盗报警功能、还能实时监测下水道水位，实时检测下水道燃气、可燃易爆气体等废气的含量，精准检测数据，实时上传至云平台。一旦发生洪涝、可燃气体泄露，智慧窨井盖监测系统就会及时报警。有关部门可根据系统实时反馈的大数据进行分析，防止重大事故的发生，保障人民群众安全。
“智慧窨井盖智能监控管理系统”采用基于智慧云谷自主研发的星云协议无线射频识别技术、传感器技术、地理信息技术、信号自动中继技术、基站联络技术、太阳能供电技术、太阳能存储技术、计算机网络技术、物联网云计算技术、大数据架构技术及其它无线通信技术等物联网相关技术，实现城市窨井盖和事件管理的智慧化、网络化和空间可视化，创新窨井盖管理新模式，完善窨井盖管理流程，建立一套科学完善的监督评价体系，并实现现有相关资源的共享，提高城市综合管理的效率和水平。该系统是多项先进技术结合的产物，超越了老式的“防盗窨井盖”、“城管通”等管理方式，真正意义上实现了窨井盖的智能化、智慧化管控模式。


智慧云谷物联网研究院
目前包括物联网空间信息联合实验室、物联网研究院培训中心、物联网研究院成果转化中心、物联网创业孵化中心。
物联网空间信息联合实验室
物联网空间信息联合实验室由智慧云谷物联网研究院与中科院遥感所共同成立。将充分发挥双方各自特长，实现产学研无缝对接、可同时满足核心技术研究和产业化需要的最佳途径。
物联网空间信息联合实验室专注于智慧城市的建设，为智慧城市提供顶层架构。政府可以通过智慧城市虚拟现实公共服务平台对城市中水、电、暖气、路灯等等进行集中管理，智慧城市的每一个子项都可以兼容到其中。从而进一步提升城市的生产效率和服务能力，加快智慧城市项目落地实施，建设智慧城市的标杆和典范。
物联网研究院培训中心
智慧云谷物联网研究院培训中心，专注于培养物联网研发人才。
智慧云谷针对目前物联网领域教材短缺的现状，与国内外知名院校合作，依据智慧云谷全球领先的精准指向物联网领域的最底层嵌入式操作系统及开发语言N语言，编写出精准指向物联网研发的教材，使物联网应用开发更加简单、安全、开放，极大的简化了开发、部署、测试和调试的难度及周期，可以快速的培训开发人员，让极其复杂的物联网底层开发技术变得简单，为物联网产业培养大量研发人才。
智慧云谷为想在物联网领域有所建树的人士提供教学和实训的机会。他们可以在智慧云谷的研发团队中进行实训，在实训中将理论与实践相结合，能够更近距离的学习物联网技术研发，充分领略物联网的魅力，从而快速培养研发能力。
对于想要创业的人士，智慧云谷还能为其提供资金、资源支持、技术指导。增加创业成功率。
物联网研究院成果转化中心
物联网研究院成果转化中心是智慧云谷为促进物联网研究院高新技术成果转化而设立的专门工作机构，致力于成为一个资源聚集、项目聚集、专家聚集的综合性成果转化服务平台和为科技型中小企业服务的重要窗口和渠道。
智慧云谷物联网研究院将汇集国内外专家。将智慧云谷全球领先的核心技术进一步深化、应用。